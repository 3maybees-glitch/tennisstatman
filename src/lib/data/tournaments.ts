export function tournamentsByMonth(): Map<string, Tournament[]> {
  const months = new Map<string, Tournament[]>();
  const sorted = [...tournaments].sort((a, b) => a.start.localeCompare(b.start));
  for (const t of sorted) {
    const month = new Date(t.start + "T00:00:00").toLocaleString("en-US", {
      month: "long",
    });
    if (!months.has(month)) months.set(month, []);
    months.get(month)!.push(t);
  }
  return months;
}
