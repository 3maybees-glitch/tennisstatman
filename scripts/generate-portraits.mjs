#!/usr/bin/env node
/**
 * Downloads photorealistic AI portraits for the top-50 ATP and WTA players.
 * Run: node scripts/generate-portraits.mjs
 */
import { mkdir, writeFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import {
  TOP_50_ATP,
  TOP_50_WTA,
  slugify,
} from "./top-50-players.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const OUT_DIR = path.join(ROOT, "public", "players");
const MANIFEST_PATH = path.join(
  ROOT,
  "src",
  "lib",
  "images",
  "portrait-manifest.ts",
);

const PROMPTS = {
  ATP: (name) =>
    `professional male tennis player portrait photograph headshot athletic ${name} studio lighting neutral dark charcoal background photorealistic sharp focus sports trading card`,
  WTA: (name) =>
    `professional female tennis player portrait photograph headshot athletic ${name} studio lighting neutral dark charcoal background photorealistic sharp focus sports trading card`,
};

function hashString(input) {
  let h = 2166136261;
  for (let i = 0; i < input.length; i++) {
    h ^= input.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return h >>> 0;
}

function portraitUrl(tour, name, slug, size = 512) {
  const prompt = encodeURIComponent(PROMPTS[tour](name));
  const seed = hashString(`${tour}:${slug}`);
  return `https://image.pollinations.ai/prompt/${prompt}?width=${size}&height=${size}&seed=${seed}&nologo=true`;
}

async function downloadPortrait(tour, name) {
  const slug = slugify(name);
  const outPath = path.join(OUT_DIR, `${slug}.jpg`);

  if (existsSync(outPath)) {
    console.log(`  skip ${slug} (exists)`);
    return slug;
  }

  const url = portraitUrl(tour, name, slug);
  const response = await fetch(url, {
    headers: { "User-Agent": "TennisStatMan-PortraitGen/1.0" },
  });

  if (!response.ok) {
    throw new Error(`Failed for ${name}: HTTP ${response.status}`);
  }

  const buffer = Buffer.from(await response.arrayBuffer());
  await writeFile(outPath, buffer);
  console.log(`  saved ${slug}.jpg (${(buffer.length / 1024).toFixed(1)} KB)`);
  return slug;
}

async function processTour(tour, names) {
  console.log(`\n${tour} — ${names.length} players`);
  const slugs = [];

  for (const name of names) {
    try {
      const slug = await downloadPortrait(tour, name);
      slugs.push(slug);
      await new Promise((r) => setTimeout(r, 1200));
    } catch (error) {
      console.error(`  error ${name}:`, error.message);
    }
  }

  return slugs;
}

async function writeManifest(slugs) {
  const sorted = [...new Set(slugs)].sort();
  const content = `/** Auto-generated by scripts/generate-portraits.mjs — do not edit manually. */
export const PORTRAIT_MANIFEST = new Set<string>([
${sorted.map((s) => `  "${s}",`).join("\n")}
]);
`;
  await writeFile(MANIFEST_PATH, content);
  console.log(`\nManifest written: ${sorted.length} portraits`);
}

async function main() {
  await mkdir(OUT_DIR, { recursive: true });

  const atpSlugs = await processTour("ATP", TOP_50_ATP);
  const wtaSlugs = await processTour("WTA", TOP_50_WTA);
  await writeManifest([...atpSlugs, ...wtaSlugs]);

  console.log("\nDone.");
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
