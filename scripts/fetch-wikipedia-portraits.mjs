#!/usr/bin/env node
/**
 * Fetches Wikipedia headshot URLs for top-50 ATP and WTA players.
 * Run: node scripts/fetch-wikipedia-portraits.mjs
 */
import { writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import {
  TOP_50_ATP,
  TOP_50_WTA,
  slugify,
} from "./top-50-players.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const OUT_PATH = path.join(ROOT, "src", "lib", "images", "wikipedia-portraits.ts");

const USER_AGENT =
  "TennisStatMan/1.0 (https://tennisstatman.vercel.app; wikipedia-portrait-fetch)";

function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

function wikiTitleCandidates(name) {
  const base = name.replace(/ /g, "_");
  const ascii = name
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/ /g, "_");
  return [...new Set([base, ascii])];
}

async function fetchJson(url) {
  const response = await fetch(url, {
    headers: { "User-Agent": USER_AGENT },
  });
  if (!response.ok) {
    throw new Error(`HTTP ${response.status}`);
  }
  return response.json();
}

async function fetchThumbByTitles(titles) {
  const url =
    "https://en.wikipedia.org/w/api.php?" +
    new URLSearchParams({
      action: "query",
      titles: titles.join("|"),
      prop: "pageimages",
      format: "json",
      pithumbsize: "512",
      redirects: "1",
    });

  const data = await fetchJson(url);
  const pages = Object.values(data.query?.pages ?? {});
  for (const page of pages) {
    if (page.thumbnail?.source) {
      return { title: page.title, url: page.thumbnail.source };
    }
  }
  return null;
}

async function fetchThumbBySearch(name) {
  const searches = [`${name} tennis`, name];
  for (const term of searches) {
    const url =
      "https://en.wikipedia.org/w/api.php?" +
      new URLSearchParams({
        action: "query",
        generator: "search",
        gsrsearch: term,
        gsrlimit: "3",
        prop: "pageimages",
        format: "json",
        pithumbsize: "512",
        redirects: "1",
      });

    const data = await fetchJson(url);
    const pages = Object.values(data.query?.pages ?? {});
    for (const page of pages) {
      const title = page.title?.toLowerCase() ?? "";
      const last = name.split(" ").pop()?.toLowerCase() ?? "";
      if (!title.includes(last)) continue;
      if (page.thumbnail?.source) {
        return { title: page.title, url: page.thumbnail.source };
      }
    }
  }
  return null;
}

async function fetchWikipediaPortrait(name) {
  const direct = await fetchThumbByTitles(wikiTitleCandidates(name));
  if (direct) return direct;

  return fetchThumbBySearch(name);
}

async function main() {
  const entries = [];
  const all = [
    ...TOP_50_ATP.map((name) => ({ tour: "ATP", name })),
    ...TOP_50_WTA.map((name) => ({ tour: "WTA", name })),
  ];

  let found = 0;
  for (const { tour, name } of all) {
    const slug = slugify(name);
    try {
      const result = await fetchWikipediaPortrait(name);
      if (result?.url) {
        entries.push({ slug, name, tour, ...result });
        found++;
        console.log(`  ✓ ${slug} ← ${result.title}`);
      } else {
        console.log(`  ✗ ${slug} (no image)`);
      }
    } catch (error) {
      console.log(`  ✗ ${slug} (${error.message})`);
    }
    await sleep(150);
  }

  const lines = entries
    .sort((a, b) => a.slug.localeCompare(b.slug))
    .map(
      (e) =>
        `  "${e.slug}": ${JSON.stringify({ url: e.url, title: e.title })},`,
    )
    .join("\n");

  const content = `/** Auto-generated by scripts/fetch-wikipedia-portraits.mjs — do not edit manually. */
export type WikipediaPortrait = {
  url: string;
  title: string;
};

export const WIKIPEDIA_PORTRAITS: Record<string, WikipediaPortrait> = {
${lines}
};
`;

  await writeFile(OUT_PATH, content);
  console.log(`\nWrote ${found}/${all.length} portraits → ${OUT_PATH}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
